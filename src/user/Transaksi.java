    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        try{
            if (nama.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Nama Wajib diisi");
                nama.requestFocus();
            }else if(bayar.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Masukan Nominal uang");
                bayar.requestFocus();
            }else{
                int a,b,c;
        
                a = Integer.parseInt(hargatot.getText());
                b = Integer.parseInt(bayar.getText());
                c = b-a;
                
                if (b < a) {
                    JOptionPane.showMessageDialog(rootPane, "Maaf uang tidak cukup!");
                    bayar.setText("");
                }else{
                    kembalian.setText(String.valueOf(c));
                    JOptionPane.showMessageDialog(rootPane, "Berhasil, silahkan klik tombol Tambah");
                    tampilData();
                    btnTambah.setEnabled(true);
                }       
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnBayarActionPerformed

