    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        try {
            stmt = conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = stmt.executeQuery("SELECT * FROM barang WHERE " + "id_barang='" + kode.getText() + "'");
            if(rs.next()){
                kode.setText(rs.getString("id_barang"));
                namaBrg.setText(rs.getString("nama_barang"));
                merk.setText(rs.getString("merk"));
                harga.setText(rs.getString("harga"));
                btnTambah.setEnabled(false);
                jumlah.setEnabled(true);
                hargatot.setEnabled(false);
                btnTransaksi.setEnabled(true);
                nama.setEnabled(false);
                cmb.setEnabled(false);
                hargatot.setEnabled(false);
                bayar.setEnabled(false);
                btnBayar.setEnabled(false);
                kembalian.setEnabled(false);
            }else{
                tampilData();
                JOptionPane.showMessageDialog(rootPane, "Kode barang tidak ditemukan!");
                reset();
                kode.requestFocus();
            }tampilData();
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCariActionPerformed

