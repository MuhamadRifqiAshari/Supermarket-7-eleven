    public void tampilData(){
        try {
            stmt = conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = stmt.executeQuery("SELECT * FROM transaksi ORDER BY id");
            while(rs.next()){
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("id_barang"),
                    rs.getString("nama_barang"),
                    rs.getString("merk"),
                    rs.getString("harga"),
                    rs.getString("jumlah_beli"),
                    rs.getString("diskon"),
                    rs.getString("harga_total"),
                    rs.getString("nama"),
                    rs.getString("bayar"),
                    rs.getString("kembalian"),
                    rs.getString("tanggal"),
                    rs.getString("waktu"),    
                };
                tabModel.addRow(data);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
